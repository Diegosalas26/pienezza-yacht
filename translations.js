// ─── Language engine ──────────────────────────────────────────────────────────

(function () {
  var STORAGE_KEY = 'pienezza_lang';

  function getLang() {
    return localStorage.getItem(STORAGE_KEY) || 'en';
  }

  function setLang(lang) {
    var t = PIENEZZA_TRANSLATIONS[lang];
    if (!t) return;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;

    // Update page title
    document.title = lang === 'es'
      ? 'Pienezza Yacht \u2014 Ch\u00e1rter Privado \u00b7 Miami'
      : 'Pienezza Yacht \u2014 Private Charter \u00b7 Miami';

    // Plain text nodes
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    // Nodes that contain inline HTML (<em>, <br>, etc.)
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // Input / textarea placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    // <option> text
    document.querySelectorAll('[data-i18n-option]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-option');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    // Testimonials — update the live array and current slide
    if (window.ts) {
      window.ts[0] = { q: t.t1_q, a: t.t1_a };
      window.ts[1] = { q: t.t2_q, a: t.t2_a };
      window.ts[2] = { q: t.t3_q, a: t.t3_a };
      var idx = window.ct || 0;
      var tQel = document.getElementById('tQ');
      var tAel = document.getElementById('tA');
      if (tQel) tQel.textContent = window.ts[idx].q;
      if (tAel) tAel.textContent = window.ts[idx].a;
    }

    // Lightbox captions — update array; refresh caption if lightbox is open
    if (window.lbPhotos) {
      window.lbPhotos[0].caption = t.interior_salon;
      window.lbPhotos[1].caption = t.interior_lounge;
      window.lbPhotos[2].caption = t.interior_galley;
      var lb = document.getElementById('lightbox');
      var lbCap = document.getElementById('lbCaption');
      if (lb && lb.classList.contains('open') && lbCap) {
        lbCap.textContent = window.lbPhotos[window.lbIndex || 0].caption;
      }
    }

    // WhatsApp message in config
    if (window.PIENEZZA_CONFIG) {
      PIENEZZA_CONFIG.whatsappMessage = t.whatsapp_message;
      if (typeof actualizarBtnWhatsApp === 'function') actualizarBtnWhatsApp();
    }

    // Form subject hidden field
    var subjectField = document.querySelector('input[name="subject"]');
    if (subjectField) {
      subjectField.value = lang === 'es'
        ? 'Pienezza Yacht \u2014 Consulta de Ch\u00e1rter'
        : 'Pienezza Yacht \u2014 Charter Inquiry';
    }

    // Toggle button active state
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.classList.toggle('lang-btn-active', btn.getAttribute('data-lang') === lang);
    });
  }

  // Expose globally so onclick handlers in HTML can call them
  window.setLang = setLang;
  window.getLang = getLang;

  document.addEventListener('DOMContentLoaded', function () {
    setLang(getLang());
  });
}());
